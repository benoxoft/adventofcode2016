import numpy as np
np.set_printoptions(threshold=np.nan, linewidth=220, formatter={'all': lambda x: ' ' if x == 0 else '#'})


def rect(line: str, display: np.array) -> np.array:
    y, x = line.split(" ")[1].split("x")
    display[:int(x), :int(y)] = 1
    return display


def rotate_row(line: str, display: np.array) -> np.array:
    tokens = line.split(" ")
    shift = int(tokens[-1])
    row = int(tokens[-3].split("=")[-1])
    display[row] = np.roll(display[row], shift)
    return display


def rotate_column(line: str, display: np.array) -> np.array:
    tokens = line.split(" ")
    shift = int(tokens[-1])
    col = int(tokens[-3].split("=")[-1])
    display[:, col] = np.roll(display[:, col], shift, )
    return display


def parse_command(line: str):
    if "rotate row" in line:
        return rotate_row
    elif "rotate column" in line:
        return rotate_column
    elif "rect" in line:
        return rect
    else:
        raise Exception("Unknown command")


def get_display():
    return np.zeros(shape=(6, 50))

